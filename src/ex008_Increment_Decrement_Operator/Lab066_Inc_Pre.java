package ex008_Increment_Decrement_Operator;

public class Lab066_Inc_Pre {
    public static void main(String[] args) {
        int a =10;
        int b = ++a;
        System.out.println(a);
        System.out.println(b);
    }

//++a -> a+1 incremented by 1

    // ERT
    //Line no  | a  | Result b
    // 5  | 10  | na
    // 6   | 11  | 11
    // 7   | 11 -> print
    // 8  | na  | 11 -> print




}
