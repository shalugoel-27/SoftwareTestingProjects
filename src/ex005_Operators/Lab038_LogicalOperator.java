package ex005_Operators;

public class Lab038_LogicalOperator {
    public static void main(String[] args) {
        boolean a = false;
        System.out.println(!a); // ! means reverse

        boolean b = false;
        System.out.println(!!b);

        boolean c = true || false;
        System.out.println(c);

        boolean c1 = true && false;
        System.out.println(c1); // rarely used in automation 0 = false ; 1 =true

    }



}
