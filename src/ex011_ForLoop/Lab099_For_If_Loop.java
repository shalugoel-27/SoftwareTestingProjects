package ex011_ForLoop;

public class Lab099_For_If_Loop {
    public static void main(String[] args) {
        for(int shruti=0; shruti<18; shruti++)
        {
            if(shruti>15)
            {
                System.out.println("Gift from papa");
            }else {
                System.out.println("no gift");
            }
        }
    }





}
